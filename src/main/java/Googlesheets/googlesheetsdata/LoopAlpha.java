package Googlesheets.googlesheetsdata;

public class LoopAlpha {

	public static void main(String[] args) {
		// Display Uppercased A to Z using for loop
		 for(char i = 'A';i <= 'Z'; ++i)
            System.out.print(i + " ");
		//Display Lowercased a to z using for loop
		char c;
        for(c = 'a'; c <= 'z'; ++c)
            System.out.print(c + " ");
	}
}
